    print("       the solution saved $200K annually. The CEO personally")
    print("       thanked me in our all-hands meeting.'")

    print("\n💡 NOTICE:")
    print("   - Specific numbers (30%, $50K, 2 weeks, 95%, 25%, $200K)")
    print("   - Clear YOUR role ('I' not 'we')")
    print("   - Business impact (not just technical fix)")
    print("   - Memorable detail (CEO thanked me)")

    print("\n🔥 COMMON STAR MISTAKES:")

    print("\n❌ MISTAKE #1: Too much Situation/Task")
    print("   ✅ FIX: Keep S+T under 25%, focus on ACTION!")

    print("\n❌ MISTAKE #2: Saying 'we' instead of 'I'")
    print("   ✅ FIX: 'I led the team that...' or 'I implemented...'")

    print("\n❌ MISTAKE #3: No numbers in Result")
    print("   ✅ FIX: Estimate if you don't know exact numbers!")

    print("\n❌ MISTAKE #4: Negative result")
    print("   ✅ FIX: Even failures should have learning/positive spin!")

