    print("   EVERY interviewer knows it.")
    print("   EVERY good answer follows it!")

    print("\n🎯 STAR = YOUR ANSWER STRUCTURE:")
    print("\n   S = SITUATION (Set the scene)")
    print("   T = TASK (What needed to be done)")
    print("   A = ACTION (What YOU did)")
    print("   R = RESULT (What happened)")

    print("\n💡 WHY STAR WORKS:")
    print("   ✅ Interviewers are trained to look for it")
    print("   ✅ Prevents rambling (gives structure)")
    print("   ✅ Focuses on YOUR actions (not team's)")
    print("   ✅ Proves impact with results")

    print("\n🔥 THE BREAKDOWN (Step by Step):")

    print("\n1️⃣ S = SITUATION (15% of answer):")
    print("   Set the context briefly!")
    print("\n   ❌ BAD: 'At my last job...'")
    print("   ✅ GOOD: 'At TechCo, our main app was crashing during")
    print("            peak hours, causing 30% user dropoff.'")
    print("\n   💡 Be specific! Numbers! Stakes!")

    print("\n2️⃣ T = TASK (10% of answer):")
    print("   What was YOUR responsibility?")
    print("\n   ❌ BAD: 'We had to fix it.'")
    print("   ✅ GOOD: 'As lead developer, I was tasked with")
    print("            identifying the bottleneck and implementing")
    print("            a solution within 2 weeks.'")
    print("\n   💡 Show it was YOUR responsibility!")

    print("\n3️⃣ A = ACTION (60% of answer):")
    print("   What did YOU do? (MOST IMPORTANT!)")
    print("\n   ❌ BAD: 'The team worked on it.'")
    print("   ✅ GOOD: 'I profiled the code, found the database query")
    print("            causing the slowdown, implemented caching,")
    print("            and tested across 10,000 simulated users.'")
    print("\n   💡 Use 'I', not 'we'! Show YOUR specific actions!")

    print("\n4️⃣ R = RESULT (15% of answer):")
    print("   What was the outcome? QUANTIFY IT!")
    print("\n   ❌ BAD: 'It worked better.'")
    print("   ✅ GOOD: 'Crashes dropped 95%, user retention increased")
    print("            25%, and the solution saved $200K annually")
    print("            in lost revenue.'")
    print("\n   💡 NUMBERS! Impact! Business value!")

    print("\n🎯 COMPLETE STAR EXAMPLE:")

    print("\n❓ QUESTION: 'Tell me about a time you solved a difficult problem.'")

    print("\n✅ STAR ANSWER:")
    print("\n   S: 'At TechCo, our main app was crashing during peak hours,")
    print("       causing 30% user dropoff and costing $50K/week.'")
    print("\n   T: 'As lead developer, I was tasked with identifying the")
    print("       bottleneck and implementing a solution within 2 weeks.'")
    print("\n   A: 'I profiled the entire codebase, discovered a database")
    print("       query running on every page load. I implemented Redis")
    print("       caching, refactored the query, and load-tested with")
    print("       10,000 simulated users.'")
