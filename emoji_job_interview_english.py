    print("\n" + "="*70)
    print("💰 SALARY NEGOTIATION - Get What You Deserve!")
    print("="*70)

    print("\n💡 THE GOLDEN RULE:")
    print("   NEVER give number first!")
    print("   Who says number first = loses negotiation")

    print("\n" + "="*70)
    print("❓ WHEN THEY ASK: 'What are your salary expectations?'")
    print("="*70)

    print("\n🎯 DEFLECTION STRATEGIES:")

    print("\n   OPTION 1: Turn it around")
    print("   'What's the budget for this role?'")
    print("   'What does this position typically pay?'")

    print("\n   OPTION 2: Give a range")
    print("   'Based on my research, similar roles in this area")
    print("    pay $80-100K. I'm flexible depending on the full")
    print("    compensation package.'")
    print("   💡 Make your LOW end = their expected HIGH end!")

    print("\n   OPTION 3: Defer")
    print("   'I'd prefer to learn more about the role and")
    print("    responsibilities before discussing numbers.'")

    print("\n❌ NEVER SAY:")
    print("   'I'm currently making $X' (they'll lowball you!)")
    print("   'I need at least $X' (shows desperation)")

    print("\n" + "="*70)
    print("💬 NEGOTIATION SCRIPTS")
    print("="*70)

    print("\n📞 WHEN THEY MAKE AN OFFER:")

    print("\n   STEP 1: Express enthusiasm first")
    print("   'Thank you so much! I'm really excited about")
    print("    this opportunity and the team.'")

    print("\n   STEP 2: Ask for time")
    print("   'Could I have 2-3 days to review the offer?'")
    print("   (Never accept immediately!)")

    print("\n   STEP 3: Come back with counter")
    print("   'I'm very excited about joining the team. Based")
    print("    on my experience and the market rate, I was")
    print("    hoping for [higher number]. Is there flexibility?'")

    print("\n💡 NEGOTIATION TIPS:")
    print("   ✅ Be confident but not aggressive")
    print("   ✅ Emphasize your value (not your needs)")
    print("   ✅ Consider total package (bonus, equity, PTO)")
    print("   ✅ Get everything in writing")

