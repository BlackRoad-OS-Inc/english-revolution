    print("   💡 Always have alternatives ready!")

    print("\n" + "="*70)
    print("📊 RESEARCH SALARY RANGES")
    print("="*70)

    print("\n🔍 BEFORE NEGOTIATING:")
    print("   ✅ Check Glassdoor, Levels.fyi, Blind")
