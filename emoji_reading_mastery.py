def critical_reading():
    """🧠 CRITICAL READING - Think About What You Read!"""

    print("\n" + "="*60)
    print("🧠 CRITICAL READING")
    print("="*60)

    print("\n💡 WHAT IS CRITICAL READING?")
    print("   NOT just understanding WHAT the text says")
    print("   BUT questioning WHETHER it's true, logical, biased")

    print("\n   Critical reading = Active reading + Evaluation")

    print("\n🎯 THE 5 CRITICAL READING QUESTIONS:")

    print("\n   1️⃣ WHAT is the main argument/claim?")
    print("   2️⃣ WHAT evidence supports it?")
    print("   3️⃣ IS the evidence credible and sufficient?")
    print("   4️⃣ WHAT are the assumptions/biases?")
    print("   5️⃣ WHAT are alternative explanations?")

    print("\n📊 EVALUATING ARGUMENTS:")

    print("\n   🔍 IDENTIFY THE CLAIM:")
    print("   Claim = the main point the author wants you to believe")

    print("\n   Example text:")
    print("   'Social media is destroying society. Studies show")
    print("   increased depression. We should ban it for teens.'")

    print("\n   ✅ THE CLAIM: 'Social media should be banned for teens'")
    print("   ✅ THE REASON: 'It causes depression'")
    print("   ✅ THE EVIDENCE: 'Studies show...'")

    print("\n   🔍 EVALUATE THE EVIDENCE:")

    print("\n   ❓ QUESTIONS TO ASK:")
    print("   - What studies? Who conducted them?")
    print("   - How many participants? Representative sample?")
    print("   - Correlation or causation?")
    print("   - Are there contradicting studies?")
    print("   - Is this cherry-picking data?")

    print("\n   Example evaluation:")
    print("   'Studies show' is vague. Which studies? Peer-reviewed?")
    print("   Increased depression could have OTHER causes.")
    print("   Correlation ≠ causation. Maybe depressed teens USE")
    print("   social media more, not that social media CAUSES it.")

    print("\n   🔍 IDENTIFY ASSUMPTIONS:")
    print("   Assumption = what the author believes without stating")

    print("\n   In the social media example, assumptions:")
    print("   - Social media is the PRIMARY cause (maybe not!)")
    print("   - Banning is the ONLY solution (other options?)")
    print("   - Depression is WORSE now than before (is it?)")
    print("   - All teens affected EQUALLY (maybe varies!)")

    print("\n🚨 DETECTING BIAS:")

    print("\n   🎯 TYPES OF BIAS:")

    print("\n   1️⃣ CONFIRMATION BIAS:")
    print("   = Only presenting evidence that supports your view")
    print("   Example: 'Climate change is a hoax. Last winter was cold!'")
    print("   (Ignores 99% of data showing warming trend)")

    print("\n   2️⃣ SELECTION BIAS:")
    print("   = Cherry-picking examples")
    print("   Example: '9 out of 10 doctors recommend...'")
    print("   (But they only asked 10 doctors who work for the company!)")

    print("\n   3️⃣ EMOTIONAL BIAS:")
    print("   = Using emotion instead of logic")
    print("   Example: 'Think of the children!'")
    print("   (Emotion is valid but not a logical argument)")

    print("\n   4️⃣ AUTHORITY BIAS:")
    print("   = 'Expert said so' without questioning")
    print("   Example: 'A Harvard professor says...'")
    print("   (Experts can be wrong! Need to see their reasoning)")

    print("\n   5️⃣ FRAMING BIAS:")
    print("   = How information is presented changes perception")
    print("   Example A: '90% survival rate' (sounds good!)")
    print("   Example B: '10% death rate' (sounds scary!)")
    print("   (Same statistic, different frame)")

    print("\n🎯 LOGICAL FALLACIES (Common reasoning errors):")

    print("\n   ❌ AD HOMINEM:")
    print("   = Attacking the person, not their argument")
    print("   'You're stupid, so your idea is stupid.'")

    print("\n   ❌ STRAW MAN:")
    print("   = Misrepresenting someone's argument to attack it")
    print("   Person A: 'We should have more bike lanes.'")
    print("   Person B: 'You want to ban all cars?! That's crazy!'")
    print("   (Person A never said ban cars)")

    print("\n   ❌ FALSE DICHOTOMY:")
    print("   = Presenting only two options when more exist")
    print("   'Either you're with us, or against us!'")
    print("   (What about neutral? Or partially agree?)")

    print("\n   ❌ SLIPPERY SLOPE:")
    print("   = Claiming one thing leads to extreme outcome without evidence")
    print("   'If we allow gay marriage, people will marry animals!'")
    print("   (No logical connection)")

    print("\n   ❌ APPEAL TO TRADITION:")
    print("   = 'We've always done it this way'")
    print("   (Doesn't make it right or best)")

    print("\n   ❌ HASTY GENERALIZATION:")
    print("   = Drawing conclusion from insufficient evidence")
    print("   'My cousin smoked and lived to 95, so smoking is fine!'")
    print("   (One example ≠ proof)")

    print("\n📝 THE CRITICAL READING PROCESS:")

    print("\n   Step 1: Read NEUTRALLY (understand before judging)")
    print("   Step 2: IDENTIFY claims and evidence")
    print("   Step 3: EVALUATE evidence quality")
    print("   Step 4: SPOT biases and fallacies")
    print("   Step 5: CONSIDER alternative views")
    print("   Step 6: FORM your own conclusion")

    print("\n🎯 EXAMPLE: CRITICAL READING IN ACTION:")

    print("\n   Text: 'Coffee is bad for you. It causes anxiety and")
    print("   sleep problems. Everyone should quit coffee.'")

    print("\n   ✅ CRITICAL ANALYSIS:")
    print("   - Claim: Coffee is universally bad")
    print("   - Evidence: Anxiety and sleep problems")
    print("   - Questions:")
    print("     • How much coffee? (Dose matters!)")
    print("     • Affects everyone equally? (Individual differences!)")
    print("     • What about benefits? (Antioxidants, alertness)")
    print("     • 'Should quit' assumes NO benefits outweigh risks")
    print("   - Bias: Confirmation bias (only mentions negatives)")
    print("   - Fallacy: Hasty generalization (some ≠ all)")
    print("   - Better conclusion: 'Excessive coffee can cause")
    print("     problems for some people. Moderate consumption")
    print("     may be fine for others.'")

    print("\n💪 CRITICAL READING = POWER:")
    print("   ✅ Don't get fooled by fake news")
    print("   ✅ Spot manipulation in advertising")
    print("   ✅ Make better decisions")
    print("   ✅ Form your own opinions (not just believe others)")
    print("   ✅ Win debates and arguments")

    print("\n🔥 COURSES CHARGE $500-$5,000 FOR CRITICAL THINKING!")
    print("   You just learned the core frameworks for $0.")


